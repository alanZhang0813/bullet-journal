package cs3500.pa05.model;

/**
 * Enum value representing each day in the days of the week.
 */
public enum DayOfWeek {
  /**
   * represents Monday of this week
   */
  MONDAY,
  /**
   * represents Tuesday of this week
   */
  TUESDAY,
  /**
   * represents Wednesday of this week
   */
  WEDNESDAY,
  /**
   * represents Thursday of this week
   */
  THURSDAY,
  /**
   * Represents Friday of this week
   */
  FRIDAY,
  /**
   * Represents Saturday of this week
   */
  SATURDAY,
  /**
   * Represents Sunday of this week
   */
  SUNDAY

}
